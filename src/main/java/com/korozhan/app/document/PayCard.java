public class PayCard {
    private String card;
    private String cardName;
    private String cardNameParam;
    private String cardNumber;
    private String cardNumberParam;
    private String cardHolder;
    private String cardHolderParam;

    public String getCard() {
        return card;
    }

    public void setCard(String card) {
        this.card = card;
    }

    public String getCardName() {
        return cardName;
    }

    public void setCardName(String cardName) {
        this.cardName = cardName;
    }

    public String getCardNameParam() {
        return cardNameParam;
    }

    public void setCardNameParam(String cardNameParam) {
        this.cardNameParam = cardNameParam;
    }

    public String getCardNumber() {
        return cardNumber;
    }

    public void setCardNumber(String cardNumber) {
        this.cardNumber = cardNumber;
    }

    public String getCardNumberParam() {
        return cardNumberParam;
    }

    public void setCardNumberParam(String cardNumberParam) {
        this.cardNumberParam = cardNumberParam;
    }

    public String getCardHolder() {
        return cardHolder;
    }

    public void setCardHolder(String cardHolder) {
        this.cardHolder = cardHolder;
    }

    public String getCardHolderParam() {
        return cardHolderParam;
    }

    public void setCardHolderParam(String cardHolderParam) {
        this.cardHolderParam = cardHolderParam;
    }
}
